              <div className="flex items-center gap-2 text-xs sm:text-sm text-neutral-600">
                <Package className="h-3 w-3 sm:h-4 sm:w-4" />
                <span>{recipe.ingredients.length} È†ÖÂéüÊñô</span>
              </div>
              
              {/* Type Badge */}
